/**
 * The Calculations class performs addition, subtraction,
 * multiplication, division, and modulus operations on integers and
 * decimals.
 *  
 * @ author APCS Team
 * @ version 2020
 */
public class CalculationsV4
{
    public static void main(String[ ] args)
    {
        // variables
        int additionNumber1 = 25;
        int additionNumber2 = 9;
        int subtractionNumber1 = 11;
        // 10.0
        int subtractionNumber3 = 10;
   
        // Addition
        System.out.println("Addition");
        System.out.println(additionNumber1 + " + " + additionNumber2 + " = " + (additionNumber1 + additionNumber2) );
        System.out.println( "43.21 + 3.14 = " + (43.21 + 3.14));
        System.out.println(" ");
        // Subtraction
        System.out.println("Subtraction");
        System.out.println("11 - 9 - 25 = " + (subtractionNumber1 - additionNumber2 - additionNumber1) );
        System.out.println( "3.14 - 10.0 = " + (3.14 - subtractionNumber3) );
        System.out.println(" ");
        // Multiplication
        System.out.println("Multiplication");
        System.out.println( "25 * 9 = " + (additionNumber1 * additionNumber2) ); 
        System.out.println( "3.14 * 10.0 * 10.0 = " + (3.14 * 10.0 * subtractionNumber3) );
        System.out.println(" ");
        // Division
        System.out.println("Division");
        System.out.println( "9 / 25 = " + (additionNumber2 / additionNumber1));
        System.out.println( "43.21 / 10.0 = " + (43.21 / subtractionNumber3) );
        System.out.println(" ");
        // Modulus operator
        System.out.println("Modulus");
        System.out.println( "11 % 9 = " + (subtractionNumber1 % additionNumber2 ) );
        System.out.println( "10.0 % 3.14" + (subtractionNumber3 % 3.14));
        System.out.println(" ");
        // 1.08 Additional int Equations
        
        
        // 1.09 Additional double Equations
        
               
    } // end of main method
} // en
